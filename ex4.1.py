# The range() Function Few example.
for i in range(6):
    print(i)


# below line '\n' means new lines
print('\n')


#range(5, 10)
for i in range(5, 10):
    print(i)

# below line '\n' means new lines
print('\n')

#range(0, 20, 2)
for i in range(0, 22, 2):
    print(i)

# below line '\n' means new lines
print('\n')

#range(-20, -200, -30)
for i in range(-20, -100, -30):
    print(i)
